const read = require("./readNumber.js");

describe('read', () => {
    test('input "1", returns "11"', () => {
        expect(read('1')).toBe('11');
    });

    test('input "12", returns "1112"', () => {
        expect(read('12')).toBe('1112');
    });

    test('input "11", returns "21"', () => {
        expect(read('11')).toBe('21');
    });

    test('input "13211311123113112211", returns "11131221133112132113212221"', () => {
        expect(read('13211311123113112211')).toBe('11131221133112132113212221');
    });

    test('input "11131221131211131231121113112221121321132132211331222113112211", returns "311311222113111231131112132112311321322112111312211312111322212311322113212221"', () => {
        expect(read('11131221131211131231121113112221121321132132211331222113112211')).toBe('311311222113111231131112132112311321322112111312211312111322212311322113212221');
    });
});
